package com.acharya.demo;

public class Nested_loop {

	public static void main(String[] args) {
		for(int i=1;i<=10;i++)
		{
			for(int j=1;j<10;j++)
			{
				System.out.println("value of i:"+i+"and j:"+j);
			}
			System.out.println();
		}
		System.out.println("***********");
	}

}
