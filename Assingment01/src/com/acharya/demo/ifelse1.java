package com.acharya.demo;

public class ifelse1 {

	public static void main(String[] args) {
		int x=5;
		
  
  if(x==5)
  {
	  System.out.println("x equals to 5");
  }
  else if(x>5)
  {
	  System.out.println("x is greater than 5");
  }
  else
  {
	  System.out.println("x is smaller than 5");
  }
}
}
